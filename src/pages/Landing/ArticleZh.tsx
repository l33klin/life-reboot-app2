        <li><strong>自我意识 (Self-Aware)</strong> — 你注意到你有一个与外部不匹配的内心生活。例如，坐在教堂里，意识到你不确定自己是否相信周围人似乎相信的东西，但还不知道该如何处理这种感觉。</li>
        <li><strong>尽责 (Conscientious)</strong> — 你建立了自己的原则体系，并对自己负责。例如，在仔细研究后离开家庭的宗教，并采用你可以捍卫的个人哲学，或者制定具有明确里程碑的职业计划，因为你相信正确的努力会产生正确的结果。</li>
        <li><strong>个人主义 (Individualist)</strong> — 你看到你的原则是由环境塑造的，并开始更宽松地持有它们。例如，意识到你的政治观点更多地与你成长的地方有关，而不是客观真理，或者注意到你雄心勃勃的职业目标实际上是为了获得父亲的认可。</li>
        <li><strong>战略家 (Strategist)</strong> — 你在意识到自己参与其中的同时与系统合作。例如，领导一个组织，同时积极质疑自己的盲点，或者参与政治，知道自己的观点是片面的，并受到自己无法完全看清的偏见的影响。</li>
        <li><strong>建构意识 (Construct-Aware)</strong> — 你将所有的框架，包括你的身份，视为有用的虚构。例如，用隐喻而不是字面意义来持有你的精神信仰，知道地图不是领土，或者带着一种温和的乐趣看着自己扮演“创始人”或“思想领袖”的角色。</li>
